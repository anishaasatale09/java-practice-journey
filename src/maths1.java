public class maths1 {
    public static void main(String[] args){
        System.out.println(Math.PI);
        System.out.println(Math.E);

        double result;

        result= Math.pow(4,2); //for power
        result = Math.sqrt(16); //for square root
        result= Math.max(100,500); //maximum
        result= Math.min(100,500); //minimum
        result= Math.abs(-5.33); //absolute value(+ve)
        result = Math.ceil(3.44); //ceiling value(high)
        result= Math.floor(3.59); //floor value(low)
        result = Math.round(3.333333339); //roundup



        System.out.println(result);
    }
}
